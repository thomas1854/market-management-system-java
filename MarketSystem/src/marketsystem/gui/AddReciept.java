    void updateList() {
        DefaultTableModel model = (DefaultTableModel) product_table.getModel();
        model.setRowCount(0);
        for (Product product : receipt.getProductPurchasedCash().keySet()) {
            model.addRow(new Object[]{
                product.getName(),
                receipt.getProductPurchasedCash().get(product),
                product.getCategory().getCategoryName(),
                product.getPrice(),
                0,
                product.getPurchasingPoints(),});
        }
        for (Product product : receipt.getProductsPoints().keySet()) {
            model.addRow(new Object[]{
                product.getName(),
                receipt.getProductPurchasedPoints().get(product),
                product.getCategory().getCategoryName(),
                0,
                product.getProductSellingPoints(),
                0,});
        }
        earned_poi_textf.setText(String.valueOf(receipt.getTotalPointsEarned()));
        spent_poi_textf.setText(String.valueOf(receipt.getTotalPointsSpent()));
        total_cost_textf.setText(String.valueOf(receipt.getTotalProductsCashCost()));
    }

