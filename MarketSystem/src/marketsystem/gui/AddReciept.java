    private void save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_btnActionPerformed
        if (cust_id.getText().isBlank() || (receipt.getProductPurchasedCash().isEmpty() && receipt.getProductPurchasedPoints().isEmpty())) {
            JOptionPane.showMessageDialog(
                    null,
                    "Please fill in all required fields.",
                    "Warning",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        try {
            marketsystem.Customer customer = CustomerRepository.getCustomerByID(Integer.parseInt(cust_id.getText()));
            if (customer.getCustomerID() == 0) {
                JOptionPane.showMessageDialog(
                        null,
                        "Customer ID does not exist in the system.",
                        "Not Found",
                        JOptionPane.WARNING_MESSAGE
                );

                return;
            }

            receipt.setCustomer(customer);
            if (customer.getPointsBalance() < receipt.getTotalPointsSpent()) {
                JOptionPane.showMessageDialog(
                        null,
                        "The customer's points balance is insufficient.",
                        "Insufficient Points",
                        JOptionPane.WARNING_MESSAGE
                );

                return;
            }
            HashMap<Product, Integer> productsAll = new HashMap<>();
            for (Product product : receipt.getProductPurchasedCash().keySet()) {
                productsAll.put(product, receipt.getProductPurchasedCash().get(product));
            }
            for (Product product : receipt.getProductPurchasedPoints().keySet()) {
                productsAll.put(product, receipt.getProductPurchasedPoints().get(product));
            }
            for (Product product : productsAll.keySet()) {
                Product productData = ProductRepository.getProductByID(product.getProductID());
                System.out.println(productData.getProductQuantity() + " " + productsAll.get(product));
                if (productData.getProductQuantity() < productsAll.get(product)) {
                    JOptionPane.showMessageDialog(
                            null,
                            "The available product quantity (" + productData.getProductQuantity() + ") units is insufficient.",
                            "Insufficient Quantity",
                            JOptionPane.WARNING_MESSAGE
                    );
                    return;
                }
            }
            ReceiptRepository.insertReceipt(receipt);
            JOptionPane.showMessageDialog(
                    null,
                    "Operation completed successfully.",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE
            );

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    null,
                    "Please enter a valid numeric value for the Customer ID.",
                    "Invalid Input",
                    JOptionPane.WARNING_MESSAGE
            );
        }
    }//GEN-LAST:event_save_btnActionPerformed

